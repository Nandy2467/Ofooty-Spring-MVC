package com.nandeep.ofooty.util;

public class KeyFacts {
	
	private Long playerCount;
	private Long teamCount;
	private Long groundCount;
	
	public KeyFacts() {
	}

	public Long getPlayerCount() {
		return playerCount;
	}

	public void setPlayerCount(Long playerCount) {
		this.playerCount = playerCount;
	}

	public Long getTeamCount() {
		return teamCount;
	}

	public void setTeamCount(Long teamCount) {
		this.teamCount = teamCount;
	}

	public Long getGroundCount() {
		return groundCount;
	}

	public void setGroundCount(Long groundCount) {
		this.groundCount = groundCount;
	}
	
}
